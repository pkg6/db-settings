namespace Pkg6\DB\Settings\Support;

use Closure;

class Call
{
    /**
     * @param mixed $value
     * @param ...$args
     * @return mixed
     */
    public static function value($value, ...$args)
    {
        return $value instanceof Closure ? $value(...$args) : $value;
    }
}
